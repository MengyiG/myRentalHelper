export function useT(lang) {
  return (key, vars) => {
    let str = t[lang]?.[key] ?? t.zh[key] ?? key;
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        str = str.replace(`{${k}}`, v);
      });
    }
    return str;
  };
}
